public class Ysfl  extends TreeNode{
	
	private String ID;	//ID
	private String MC;	//名称
	private String PID;	//PID
	private String PXH;	//排序号
	private String ZT;	//状态(0:停用;1:启用;9:删除)
	private String target;
	private Ysfl parentMenu;
	private List<Ysfl> subMenu;
	private boolean hasMenu = false;
	private String treeurl;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getMC() {
		return MC;
	}
	public void setMC(String mC) {
		MC = mC;
	}
	public String getPID() {
		return PID;
	}
	public void setPID(String pID) {
		PID = pID;
	}
	public String getPXH() {
		return PXH;
	}
	public void setPXH(String pXH) {
		PXH = pXH;
	}
	public String getZT() {
		return ZT;
	}
	public void setZT(String zT) {
		ZT = zT;
	}
	public String getTarget() {
		return target;
	}
	public void setTarget(String target) {
		this.target = target;
	}
	public Ysfl getParentMenu() {
		return parentMenu;
	}
	public void setParentMenu(Ysfl parentMenu) {
		this.parentMenu = parentMenu;
	}
	public List<Ysfl> getSubMenu() {
		return subMenu;
	}
	public void setSubMenu(List<Ysfl> subMenu) {
		this.subMenu = subMenu;
	}
	public boolean isHasMenu() {
		return hasMenu;
	}
	public void setHasMenu(boolean hasMenu) {
		this.hasMenu = hasMenu;
	}
	public String getTreeurl() {
		return treeurl;
	}
	public void setTreeurl(String treeurl) {
		this.treeurl = treeurl;
	}
	
	
	

}
